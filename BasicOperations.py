# Basic arithmetic operations
a = 10
b = 5
print("Addition:", a + b)            # Addition
print("Subtraction:", a - b)         # Subtraction
print("Multiplication:", a * b)      # Multiplication
print("Division:", a / b)            # Division
print("Floor Division:", a // b)     # Floor division
print("Modulus:", a % b)             # Modulus
print("Exponentiation:", a ** b)     # Exponentiation

# Mathematical functions
import math
x = -5.8
print("Absolute value:", abs(x))
print("Square root:", math.sqrt(25))
print("Power:", pow(2, 3))           # Equivalent to 2**3
print("Round:", round(3.14159, 2))   # Round to 2 decimal places
print("Ceiling:", math.ceil(4.2))
print("Floor:", math.floor(4.8))
print("Logarithm:", math.log(10))    # Natural log
print("Logarithm base 10:", math.log10(100))
